$servername = "localhost";
$username = "root";
$password = "";
$database = "admintf";

// Create connection
$conn = new mysqli($servername, $username, $password, $database);

// Check connection
if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

// Check if form is submitted
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Retrieve form data
    $email = $_POST['email'];
    $password = $_POST['password'];

    // Validate login
    $sql = "SELECT * FROM aregister WHERE email = ? AND password = ?";
    $stmt = $conn->prepare($sql);
    $stmt->bind_param("ss", $email, $password);
    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        // Credentials are valid, proceed with login
        echo "<script>alert('Login successful!');</script>";
        // Redirect to dashboard or another page
        header("Location: aindex.php");
        exit(); // Exit after redirection
    } else {
        // Credentials are not valid, show alert
        echo "<script>alert('Invalid email or password. Please try again.');</script>";
    }

    // Insert entered details into database (assuming register table structure)
    $sql_insert = "INSERT INTO auser (email, password) VALUES ($email, $password)";
    $stmt_insert = $conn->prepare($sql_insert);
    $stmt_insert->bind_param("ss", $email, $password);
    $stmt_insert->execute();
}

// Close database connection
$conn->close();
